function coreProps() {
  return xml(`<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<cp:coreProperties xmlns:cp="http://schemas.openxmlformats.org/package/2006/metadata/core-properties" xmlns:dc="http://purl.org/dc/elements/1.1/" xmlns:dcterms="http://purl.org/dc/terms/" xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance">
  <dc:creator>AUTARQ Office</dc:creator>
  <cp:lastModifiedBy>AUTARQ Office</cp:lastModifiedBy>
  <dcterms:created xsi:type="dcterms:W3CDTF">${now()}</dcterms:created>
  <dcterms:modified xsi:type="dcterms:W3CDTF">${now()}</dcterms:modified>
</cp:coreProperties>`);
}

function appProps(application) {
  return xml(`<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Properties xmlns="http://schemas.openxmlformats.org/officeDocument/2006/extended-properties" xmlns:vt="http://schemas.openxmlformats.org/officeDocument/2006/docPropsVTypes">
  <Application>${application}</Application>
</Properties>`);
}

function xml(value) {
  return value.trim();
}

export function blankFileName(ext) {
  const label = FILE_TYPES[ext]?.label || "Document";
  return `Untitled ${label}.${ext}`;
}
